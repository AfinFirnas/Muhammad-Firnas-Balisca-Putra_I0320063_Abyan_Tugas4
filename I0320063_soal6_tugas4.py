#SOAL 6

x = 4
y = 11

#a
a = x | y
print('x | y =', a, 'dengan biner', bin(a))

#b
b = x >> y
print('x >> y =', b, 'dengan biner', bin(b))

#c
c = x ^ y
print('x ^ y =', c, 'dengan biner', bin(c))

#d
d = ~x
print('~x =', d, 'dengan biner', bin(d))

#e
e = y & x
print('y & x =', e, 'dengan biner', bin(e))


